goTeam = ("Go, team, go!")
defeat = ("Defeat your foe.")
simply = ("Simply the best,")
better = ("Better than the rest.")

def firstVerse():
    print(goTeam)
    print(defeat)

def secondVerse():
    print(goTeam)
    print(defeat)
    print(simply)
    print(better)
    print(goTeam)
    print(defeat)

def space():
    print("            ")  

def sing_fight_song():
    firstVerse()
    space()
    secondVerse()
    space()
    secondVerse()
    space()
    firstVerse()
    

sing_fight_song()
